    public void dynamicSearch(String search){
        TableRowSorter<DefaultTableModel> table = new TableRowSorter<DefaultTableModel>(model);
        employeeTable.setRowSorter(table);
        table.setRowFilter(RowFilter.regexFilter(search));
    }

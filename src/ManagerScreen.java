    public void showEmployees(){
        model.setRowCount(0);//resets the old table
        ArrayList<Employee> employees = new ArrayList<Employee>();//create a  new arraylist
        
        employees = operations.bringEmployees();
        if(employees != null){
            for(Employee employee : employees){
                Object[] toAdd = {employee.getId() , employee.getName(), 
                    employee.getSurname(),  employee.getDepartman(),employee.getSalary(), employee.getUsername() , employee.getPassword() , employee.isIsadmin()};
                
                model.addRow(toAdd);
            }
        }
        
    }

